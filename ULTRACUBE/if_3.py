a=int(input("\nEnter a number to see if it is bigger or smaller than 100: "))
if a>100:
    print(f"\nThe number you entered ({a}) is bigger than 100\n")
elif a==100:
    print(f"\nThe number you entered ({a}) is equal to 100\n")
else:
    print(f"\nThe number you entered ({a}) is smaller than 100\n")